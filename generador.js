function generarlistadecartones(){
    
      for (var i=cantidadcartones;i>=1;i--){     

        var factorydiv = document.createElement("DIV");
        factorydiv.setAttribute("id","carton".concat(i));
        factorydiv.setAttribute("style","position: relative; display: inline-block; text-align: center;");
        factorydiv.setAttribute("data-toggle","modal");
        factorydiv.setAttribute("data-target","#VentanaModalcarton");
        factorydiv.setAttribute("onclick","generarcarton".concat("(",i,");"));        
        document.getElementById('contenidolistadecartones').insertBefore(factorydiv,document.getElementById("contenidolistadecartones").childNodes[0]);
      
       
        var factoryspan = document.createElement("SPAN");
        factoryspan.setAttribute("id","labelcarton".concat(i));
        factoryspan.setAttribute("style","font-size: 30px ;-webkit-text-fill-color: #F5FFF5;-webkit-text-stroke: 0.8px black; position: absolute;top: 45%;left: 50%;transform: translate(-50%, -50%);");
      
        document.getElementById("carton".concat(i)).insertBefore(factoryspan,document.getElementById("carton".concat(i)).childNodes[0]);
      
        document.getElementById("labelcarton".concat(i)).innerHTML="<strong>".concat(i,"</strong>");

        var factoryimg = document.createElement("IMG");
        factoryimg.setAttribute("id","cartonimg".concat(i));
        factoryimg.setAttribute("src",fondodecarton);

        factoryimg.setAttribute("style","margin: 1px 1px 1px 1px;");
        factoryimg.setAttribute("width","70");
        factoryimg.setAttribute("height","100");        
        document.getElementById("carton".concat(i)).insertBefore(factoryimg,document.getElementById("carton".concat(i)).childNodes[0]);  
      }       
}




  
